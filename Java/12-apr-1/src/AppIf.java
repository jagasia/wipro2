
public class AppIf {

	
	public static void main(String[] args) {
		int i=20;
		if(i>=90)
		{
			System.out.println("Excellent");
		}else if(i>=80)
		{
			System.out.println("Good");
		}else if(i>=70)
		{
			System.out.println("Fair");
		}
		else if(i>=50)
		{
			System.out.println("Pass");
		}
		else
		{
			System.out.println("Fail");
		}
	
	}

}
